void main() {
  /*
  Keywords:
          Pre-define and reserve words that conway a meaningful message to compiler
          In dart programming we have 61 keywords
          i.e -> static, var, dynamic , final, const, string , int etc
  */

  //1)Int
  int a;
  a = 10;
  print(a);

  //2)Const -> Assign value once compile time
  const int num = 5;
  print(num);

  //3)Final -> Assign value once run-time
  final int f;
  f = 2;
  print(f);
}
