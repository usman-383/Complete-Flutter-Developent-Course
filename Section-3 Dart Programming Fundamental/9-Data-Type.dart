  //Declaration
  String name;

  //Assigning
  name = "Usman";

  //output
  print("Welcome $name");

  //4)Double -> Use for storing floating numbers
  //Declaration
  double d;

  //Assigning
  d = 10.0;

  //Output
  print(d);

  //5)Char -> Use for storing single character
  //Declaration
  String ch;

  //Assigning
  ch = 'U';

  //Output
  print(ch);

